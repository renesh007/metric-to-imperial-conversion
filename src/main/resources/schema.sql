DROP TABLE IF EXISTS EXPRESSIONMAPPING cascade;
DROP TABLE IF EXISTS FACTORMAPPING cascade;
DROP TABLE IF EXISTS TYPE cascade;
DROP TABLE IF EXISTS UNIT cascade;

CREATE TABLE TYPE(
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NAME VARCHAR(255),
	ISEXPRESSION BOOLEAN
);


CREATE TABLE UNIT(
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NAME VARCHAR(255),
	SYSTEM VARCHAR(25),
	TYPE INT,
FOREIGN KEY(TYPE) REFERENCES TYPE(ID)
);


CREATE TABLE FACTORMAPPING(
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	FROMUNIT INT,
	TOUNIT INT,
	MULTIPLIER FLOAT(25),
FOREIGN KEY(FROMUNIT) REFERENCES UNIT(ID),
FOREIGN KEY(TOUNIT) REFERENCES UNIT(ID)
);


CREATE TABLE EXPRESSIONMAPPING(
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	FROMUNIT INT,
	TOUNIT INT,
	EQUATION VARCHAR(255),
FOREIGN KEY(FROMUNIT) REFERENCES UNIT(ID),
FOREIGN KEY(TOUNIT) REFERENCES UNIT(ID)
);